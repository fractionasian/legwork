#!/usr/bin/env node
// Hashes the user-facing app shell files and writes the result to
// sw-version.js so the service worker invalidates its cache on each release.

const fs = require("fs");
const path = require("path");
const crypto = require("crypto");

const ROOT = path.join(__dirname, "..");
const SHELL = ["app.js", "style.css", "index.html", "sw.js"];

const hash = crypto.createHash("md5");
for (const f of SHELL) {
    hash.update(fs.readFileSync(path.join(ROOT, f)));
}
const version = hash.digest("hex").substring(0, 12);

const out = `// Auto-generated by scripts/update-sw-version.js
// MD5 hash of app.js + style.css + index.html + sw.js, regenerated on each release.
var SW_VERSION = "${version}";
`;
fs.writeFileSync(path.join(ROOT, "sw-version.js"), out);
console.log(`sw-version.js -> ${version}`);
